public class Dump1090PropertiesException extends Exception {

    public Dump1090PropertiesException() {
        super();
    }

    public Dump1090PropertiesException(Throwable cause) {
        super(cause);
    }

    public Dump1090PropertiesException(String message) {
        super(message);
    }

    public Dump1090PropertiesException(String message, Throwable cause) {
        super(message, cause);
    }

    protected Dump1090PropertiesException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
